package model;

/**
 *
 * @author ivanmoya
 */
public class RulesDefinitions {
    private String poemRule;
    private String lineRule;
    private String adjectiveRule;
    private String nounRule;
    private String verbRule;
    private String prepositionRule;
    private String pronounRule;

    /**
     * @return the poemRule
     */
    public String getPoemRule() {
        return poemRule;
    }

    /**
     * @param poemRule the poemRule to set
     */
    public void setPoemRule(String poemRule) {
        this.poemRule = poemRule;
    }

    /**
     * @return the lineRule
     */
    public String getLineRule() {
        return lineRule;
    }

    /**
     * @param lineRule the lineRule to set
     */
    public void setLineRule(String lineRule) {
        this.lineRule = lineRule;
    }

    /**
     * @return the adjectiveRule
     */
    public String getAdjectiveRule() {
        return adjectiveRule;
    }

    /**
     * @param adjectiveRule the adjectiveRule to set
     */
    public void setAdjectiveRule(String adjectiveRule) {
        this.adjectiveRule = adjectiveRule;
    }

    /**
     * @return the nounRule
     */
    public String getNounRule() {
        return nounRule;
    }

    /**
     * @param nounRule the nounRule to set
     */
    public void setNounRule(String nounRule) {
        this.nounRule = nounRule;
    }

    /**
     * @return the verbRule
     */
    public String getVerbRule() {
        return verbRule;
    }

    /**
     * @param verbRule the verbRule to set
     */
    public void setVerbRule(String verbRule) {
        this.verbRule = verbRule;
    }

    /**
     * @return the prepositionRule
     */
    public String getPrepositionRule() {
        return prepositionRule;
    }

    /**
     * @param prepositionRule the prepositionRule to set
     */
    public void setPrepositionRule(String prepositionRule) {
        this.prepositionRule = prepositionRule;
    }

    /**
     * @return the pronounRule
     */
    public String getPronounRule() {
        return pronounRule;
    }

    /**
     * @param pronounRule the pronounRule to set
     */
    public void setPronounRule(String pronounRule) {
        this.pronounRule = pronounRule;
    }
}
